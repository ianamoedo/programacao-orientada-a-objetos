public class Eventos {
    String nome;
    String data;
    String local;
    int numeroMaxParticipantes;


    Eventos(String nome, String data, String local, int numeroMaxParticipantes) {
        this.nome = nome;
        this.data = data;
        this.local = local;
        this.numeroMaxParticipantes = numeroMaxParticipantes;
    }
}
